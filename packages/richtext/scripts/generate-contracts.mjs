import { spawnSync } from 'node:child_process';
import { cp, mkdtemp, readFile, rm, writeFile } from 'node:fs/promises';
import { tmpdir } from 'node:os';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const repositoryRoot = resolve(packageRoot, '../..');
const generated = resolve(packageRoot, 'src/generated');
const temporary = await mkdtemp(resolve(tmpdir(), 'rustok-richtext-contracts-'));
try {
  const cargo = spawnSync(
    'cargo',
    ['run', '--quiet', '-p', 'rustok-content', '--example', 'export_richtext_contracts', '--', temporary],
    { cwd: repositoryRoot, stdio: 'inherit', shell: process.platform === 'win32' }
  );
  if (cargo.status !== 0) process.exit(cargo.status ?? 1);
  const schema = JSON.parse(await readFile(resolve(temporary, 'document.schema.json'), 'utf8'));
  if (schema?.type !== 'object' || schema?.properties?.type?.type !== 'string') {
    throw new Error('Unexpected RichTextDocument JSON schema');
  }
  const profiles = JSON.parse(await readFile(resolve(temporary, 'profiles.json'), 'utf8'));
  const profileIds = profiles.map((profile) => `'${profile.id}'`).join(' | ');
  await cp(resolve(temporary, 'document.schema.json'), resolve(generated, 'document.schema.json'));
  await cp(resolve(temporary, 'profiles.json'), resolve(generated, 'profiles.json'));
  await writeFile(resolve(generated, 'contracts.ts'), contracts(profileIds));
  await writeFile(
    resolve(generated, 'profiles.ts'),
    `// @generated by scripts/generate-contracts.mjs. Do not edit by hand.\n\nimport rawProfiles from './profiles.json' with { type: 'json' };\nimport type { RichTextProfileManifest } from './contracts';\n\nexport const RICH_TEXT_PROFILES = rawProfiles as readonly RichTextProfileManifest[];\n`
  );
} finally {
  await rm(temporary, { recursive: true, force: true });
}

function contracts(profileIds) {
  return `// @generated by scripts/generate-contracts.mjs. Do not edit by hand.

export interface RichTextMark {
  type: string;
  attrs?: Record<string, unknown>;
}

export interface RichTextNode {
  type: string;
  attrs?: Record<string, unknown>;
  content?: RichTextNode[];
  marks?: RichTextMark[];
  text?: string;
}

export interface RichTextDocument {
  type: 'doc';
  content: RichTextNode[];
}

export interface RichTextLimits {
  max_json_bytes: number;
  max_depth: number;
  max_nodes: number;
  max_text_chars: number;
  max_marks_per_node: number;
  max_links: number;
  max_attribute_bytes: number;
  max_url_bytes: number;
}

export interface RichTextProfileManifest {
  id: RichTextProfileId;
  nodes: string[];
  marks: string[];
  heading_levels: number[];
  allow_empty: boolean;
  external_link_rel: string;
  limits: RichTextLimits;
}

export type RichTextProfileId = ${profileIds};
`;
}
